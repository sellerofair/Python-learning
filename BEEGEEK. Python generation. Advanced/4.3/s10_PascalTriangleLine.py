triangle = [
    [1],
    [1, 1]
]

n = int(input())

if n > 1:
    for i in range(2, n + 1):
        line = [1]
        prev_line = triangle[i - 1]
        side_number = (i + 1) // 2
        need_middle = (i + 1) % 2
                
        for j in range(1, side_number):
            line.append(prev_line[j - 1] + prev_line[j])
        
        if need_middle:
            line.append(prev_line[side_number - 1] + prev_line[side_number])
        
        for j in range(side_number - 1, -1, -1):
            line.append(line[j])
        
        triangle.append(line)

print(triangle[n])